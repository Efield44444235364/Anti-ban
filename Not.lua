-- 1. ฟังก์ชันสำหรับเรียกคำสั่งที่ "ถูกต้อง" (game:Shutdown())
local function callCorrectShutdown()
    -- คำสั่งนี้ใช้สำหรับปิดเซิร์ฟเวอร์
    game:Shutdown()
end
---
-- 2. ฟังก์ชันสำหรับเรียกคำสั่งที่ "ไม่ถูกต้อง" (game:ShutDown() - ตัว D ใหญ่)
local function callIncorrectShutdown()
    -- ฟังก์ชันนี้ไม่มีอยู่จริงและจะทำให้เกิด Error ทันทีที่ถูกเรียก
    game:ShutDown() 
end
---
-- ** การเรียกใช้งาน **

-- เรียกฟังก์ชันที่ 1: การเรียกที่ถูกต้อง
-- (ถ้าถูกรันในเซิร์ฟเวอร์จริง จะเริ่มทำการปิดเซิร์ฟเวอร์)
callCorrectShutdown()

-- เรียกฟังก์ชันที่ 2: การเรียกที่ผิดพลาด
-- (การเรียกนี้จะทำให้เกิด Error และหยุดการทำงานของสคริปต์)
callIncorrectShutdown() 

